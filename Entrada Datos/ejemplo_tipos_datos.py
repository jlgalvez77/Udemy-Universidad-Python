# Ejemplo de concatenación o suma de valores
numero1_cadena = '123'
print(f'Número 1 en cadena: {numero1_cadena}')
numero2_cadena = '456'
print(f'Número 2 en cadena: {numero2_cadena}')
resultado = numero1_cadena + numero2_cadena
print(f'Resultado de la concatenación: {resultado}')

# Convertir de cadena a entero
numero1 = int(numero1_cadena)
print(f'Número 1 en entero: {numero1}')
numero2 = int(numero2_cadena)
print(f'Número 2 en entero: {numero2}')
resultado = numero1 + numero2
print(f'Resultado de la suma: {resultado}')