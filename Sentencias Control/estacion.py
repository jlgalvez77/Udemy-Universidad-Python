mes = int(input('Introduce un mes en su valor númerico(1-12)'))

if mes == 1 or mes == 2 or mes == 12:
    print('Es Invierno')
elif mes == 3 or mes == 4 or mes == 5:
    print('Es Primavera')
elif mes == 6 or mes == 7 or mes == 8:
    print('Es Verano')
elif mes == 9 or mes == 10 or mes == 11:
    print('Es Otoño')