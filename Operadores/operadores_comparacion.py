# Operadores de ComparaciÃ³n
a = 10
b = 15
print(f'Valor inicial de a: {a}, b: {b}')

# Operador de igualdad ==
c = a == b
print(f'a == b -> {c}')

# Operador mayor que >
c = a > b
print(f'a > b -> {c}')

# Operador mayor o igual que >=
c = a >= b
print(f'a >= b -> {c}')

# Operador menor que <
c = a < b
print(f'a < b -> {c}')

# Operador menor o igual que <=
c = a <= b
print(f'a <= b -> {c}')

# Operador negacion, diferente que !=
c = a != b
print(f'a != b -> {c}')