condicion1 = False
condicion2 = True

# Operador logico and. Devuelve True si ambos operandos son True
resultado = condicion1 and condicion2
print(f'El resultado de {condicion1} and {condicion2} es: {resultado}')
# Operador logico or. Devuelve True si uno de los operandos es True
resultado = condicion1 or condicion2
print(f'El resultado de {condicion1} or {condicion2}; es {resultado}')
# Operador logico not. Invierte el valor del operando
resultado = not condicion1
print(f'El resultado de Not {condicion1} es {resultado}')