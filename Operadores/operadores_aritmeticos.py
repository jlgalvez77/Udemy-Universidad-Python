a = 10
b = 3

# Suma +
suma = a + b
print(f'El resultado de la suma es: {suma}')

# Resta -
resta = a - b
print(f'El resultado de la resta es: {resta}')

# Multiplicación *
multiplicacion = a * b
print(f'El resultado de la multiplicación es: {multiplicacion}')

# División /
division = a / b
print(f'El resultado de la división es: {division:.2f}')

# División entera //
division_entera = a // b
print(f'El resultado de la dividión entera es: {division_entera}')

# Modulo %
modulo = a % b
print(f'El resultado del modulo es: {modulo}')

# Exponente **
exponente = a ** b
print(f'El resultado de la exponenciación es: {exponente}')